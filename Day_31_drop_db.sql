-- SQL DROP DATABASE Statement 
-- For drop the existing sql database
-- Syntax : DROP DATABASE Database name;
-- Example : Drop the database called 'testDB':

DROP DATABASE testDB ;

/* Be careful before dropping a database. Deleting a database will result in loss of complete information stored in the database! */

/* Tip: Make sure you have admin privilege before dropping any database. Once a database is dropped, you can check it in the list of databases with the following SQL command: SHOW DATABASES; */
